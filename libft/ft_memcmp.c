#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*uc1;
	unsigned char	*uc2;

	uc1 = (unsigned char *)s1;
	uc2 = (unsigned char *)s2;
	i = 0;
	while (i++ < n)
		if (uc1[i - 1] != uc2[i - 1])
			return ((int)(uc1[i - 1] - uc2[i - 1]));
	return (0);
}
